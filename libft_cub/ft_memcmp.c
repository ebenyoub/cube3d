#include "libft_cub.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const unsigned char *p1;
	const unsigned char *p2;

	p1 = s1;
	p2 = s2;
	while (n--)
	{
		if (*p1 != *p2)
			return (*p1 - *p2);
		else
		{
			p1++;
			p2++;
		}
	}
	return (0);
}

int		main()
{
	char s1[] = "\200";
	char s2[] = "\0";
	printf("moi : %d\n", ft_memcmp(s1, s2, 5));
	printf("lui : %d\n", memcmp(s1, s2, 5));
	return (0);
}

